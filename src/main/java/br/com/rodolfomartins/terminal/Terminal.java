package br.com.rodolfomartins.terminal;

/**
 * Descrição do Fonte
 * 
 * @author 15/09/2015: Rodolfo Martins <DD>
 */
public class Terminal
{

   private Integer ptacod;
   private String tercod;
   private String teride;
   private String ternom;

   public Integer getPtacod()
   {
      return ptacod;
   }

   public void setPtacod(Integer ptacod)
   {
      this.ptacod = ptacod;
   }

   public String getTercod()
   {
      return tercod;
   }

   public void setTercod(String tercod)
   {
      this.tercod = tercod;
   }

   public String getTeride()
   {
      return teride;
   }

   public void setTeride(String teride)
   {
      this.teride = teride;
   }

   public String getTernom()
   {
      return ternom;
   }

   public void setTernom(String ternom)
   {
      this.ternom = ternom;
   }

}
